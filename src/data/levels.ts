const THEME_IDS = ["cocktail", "lab", "magic", "relic"] as const;

function buildLevels(): LevelConfig[] {
  return BOTTLE_COUNTS.map((count, i) => {
    const number = i + 1;
    // Rotate themes with a shift every 6 levels so nearby levels feel different
    const shift = Math.floor(i / 6);
    const themeIndex = (i + shift) % 4;
    return {
      id: `level_${String(number).padStart(3, "0")}`,
      number,
      bottleCount: count,
      themeId: THEME_IDS[themeIndex],
      judgeLimit: null,
      timeLimit: null,
    };
  });
}

export const LEVELS: LevelConfig[] = buildLevels();
